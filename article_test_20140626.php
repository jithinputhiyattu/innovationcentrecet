      <p class ="justify">Do you have an Idea waiting to be discovered? Are you clueless about what to do next? Do you need help to realize that idea of yours? Well worry no more for we have the perfect solution-The Innovation Centre. Gone are those days when the students had to run across staff rooms for a permission letter to use the lab facilities of the College. New facilities that shape the dreams of many students have made life easier now.
The Innovation Centre was conceived way back in 2012 to address the problems faced by the students in realizing innovative projects and ideas. It was Dr N Asok Kumarâ€™s (Professor, Dept. of Mechanical Engineering, CET) persistent efforts that made the Innovation Centre become a reality. The centre was setup with financial assistance from Centre for Engineering Research and Development (CERD). Later on, the structure and functioning was formulated with students in the forefront. The initial student team included Gokul Sreenivas, Ginto Devassikuty, Gopu Chandran, Ajith Kocheril, Mahadev Gopalakrishnan,Hameem C Hamza, Haseel, Salah Abdul Gafoor, Krishna Raj, Tarun U, Anoop Sasidharan and various other students from different branches. The student committee was headed and guided by Dr N Asok Kumar, staff coordinator of the Innovation Centre. 
The basic idea and objective of setting up such a facility is to nurture the innovative and creative thinking of students. It also provides a platform for them to realize and express their ideas and takes their perspectives to new levels. To fulfill this dream, a Fabrication lab was set up at the main block which consists of various tools and equipments for fabricating and testing various innovative projects. Tools and equipment such as Bosch drill kit, Angle grinder, Jigsaw, DSO, Multimeters and various basic mechanical tools are now available round the clock. In order to access these facilities a student has to contact the Student Coordinator or the person in charge and fill in a request form. Thus the procedure to be followed is simplified and fabrication has now become a hassle free process. 
Apart from the fabrication lab, the Innovation centre also purchased NI Labview software, which is a simulation software for instrumentation. The software is installed at various labs including the CTSE and students are given training to use it.
We believe that innovation comes through Creativity, Knowledge and Motivation. In order to improve the knowledge and provide encouragement various events, seminars and training programs are conducted covering various subjects such as robotics, computer hardware assembly, android development, fabrication, Air conditioning, hydraulics, pneumatics, automation etc. 
Also one hour of every Thursday is set aside for brain storming sessions, Technical talks, Seminar, Group discussion and showing latest technical videos. The aim of these events is to be the spark that ignites technical interests in the minds of students fuelled by unabating inspiration.
For conducting all these activities various committees involving students have been formed for matters like Event Management, Content Management, Room Management and Training Coordination. The students in need of any help may contact the committee member or post their ideas on the website, <a href="www.innovationcentrecet.org " >www.innovationcentrecet.org </a>or even mail us at innovationcentrecet@gmail.com. People are free to directly contact the staff coordinator, Dr. N Asok Kumar to seek help.
Thus by providing these facilities, the Innovation Centre focuses on improving the overall technical atmosphere of the college and encouraging the students to come up with original ideas leading to new discoveries that rewrite history.

